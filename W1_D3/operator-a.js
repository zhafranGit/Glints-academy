// operator assignment ( = )

let angka = 10
angka = 5
console.log("Ini angka", angka)

// operator assignment ( += )

let angka1 = 10
angka1 += 5
console.log("Ini angka1", angka1)

// operator assignment ( -= )

let angka2 = 10
angka2 -= 2
console.log("Ini angka2", angka2)

// operator assignment ( *= )

let angka3 = 10
angka3 *= 2
console.log("Ini angka3", angka3)

// operator assignment ( /= )

let angka4 = 10
angka4 /= 2
console.log("Ini angka4", angka4)

